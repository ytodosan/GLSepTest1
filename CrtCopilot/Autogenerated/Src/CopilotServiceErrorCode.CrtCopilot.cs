namespace Creatio.Copilot
{

	#region Class: CopilotServiceErrorCode

	/// <summary>
	/// Provides error codes for the Copilot service.
	/// </summary>
	public static class CopilotServiceErrorCode
	{

		#region Fields: Public

		/// <summary>
		/// Represents an unknown error.
		/// </summary>
		public static readonly string UnknownError = nameof(UnknownError);

		/// <summary>
		/// Represents an error where the action execution failed.
		/// </summary>
		public static readonly string ActionExecutionFailed = nameof(ActionExecutionFailed);

		/// <summary>
		/// Represents an error where there are insufficient permissions.
		/// </summary>
		public static readonly string InsufficientPermissions = nameof(InsufficientPermissions);

		#endregion

	}

	#endregion

}

