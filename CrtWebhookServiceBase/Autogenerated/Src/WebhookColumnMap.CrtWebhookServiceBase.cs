namespace Terrasoft.Configuration
{

    #region Class: WebhookColumnMap

    /// <summary>
    /// Contains the mapping logic for the webhook column name and entity column name.
    /// </summary>
    public class WebhookColumnMap
    {
        /// <summary>
        /// Gets or sets the name of the webhook field.
        /// </summary>
        public string WebhookColumnName { get; set; }

        /// <summary>
        /// Gets or sets the name of the entity column.
        /// </summary>
        public string EntityColumnName { get; set; }
	}

	#endregion

}

